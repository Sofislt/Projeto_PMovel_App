import 'dart:ui';
import 'package:flutter/material.dart';
import 'package:projetofelype/Domain/frase.dart';

class AllQuoteCard extends StatefulWidget {
  final List<Frase> frases;
  final List<Autogenerated> frasesApiReal;

  const AllQuoteCard({
